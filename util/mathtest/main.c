#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main(int argc, char **argv) {
	double x;

	if (argc > 1) {
		x = strtod(argv[1], NULL);
	}
	else {
		x = 42.42424242424242;
	}

	printf("x = %f = %e ~= %d\n", x, x, (int) x);

	return 0;
}
